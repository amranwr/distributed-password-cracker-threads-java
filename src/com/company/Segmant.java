package com.company;

public class Segmant {
	char s;
	int frequency;
	public Segmant(char s, int frequency) {
		this.frequency =frequency;
		this.s = s;
	}
	public char getS() {
		return s;
	}
	public void setS(char s) {
		this.s = s;
	}
	public int getFrequency() {
		return frequency;
	}
	public void setFrequency(int frequency) {
		this.frequency = frequency;
	}
	
	
}
